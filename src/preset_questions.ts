export type ChatPerspective = 'integrated' | 'buffett' | 'wangyuquan' | 'wangchuan'

/** 视角自动匹配规则 */
export function pickPerspective(question: string): ChatPerspective {
  const q = question.toLowerCase()
  // 王煜全：技术/产业格局/护城河/四维
  if (/(技术领先|护城河|产业格局|产业链|菱形|四维|工程化|颠覆|增强)/.test(question)) return 'wangyuquan'
  // 王川：垄断/客户/路线图/加仓减仓/八教训
  if (/(垄断|临界点|加仓|减仓|路线图|八教训|气急败坏|10倍|改错)/.test(question)) return 'wangchuan'
  // 巴菲特：价格/估值/股息/能力圈/安全边际
  if (/(价格|估值|股息|分红|能力圈|安全边际|现金流|好生意|好价格|护城河之外)/.test(question)) return 'buffett'
  // 默认综合
  if (/value|price|moat|tech|monopoly/i.test(q)) return 'integrated'
  return 'integrated'
}

